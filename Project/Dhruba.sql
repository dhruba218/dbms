--- CREATING TABLES 

CREATE TABLE USERS(
    USER_ID	        CHAR(5),
    FIRST_NAME	    CHAR(50),
    LAST_NAME	    CHAR(50),
    USER_PASSWORD	VARCHAR(50),
    EMAIL_ID	    VARCHAR(50),
    USER_ROLE	    VARCHAR(50),
    WALLETS	        NUMBER(8,2));
    
CREATE TABLE USERS_CONTACT(
    USER_ID	CHAR(5)	PRIMARY KEY,
    MOBILE_NUMBER	CHAR(10));
        
CREATE TABLE VEHICLE(			
    VEHICLE_MODEL	    VARCHAR2(50),		
    VEHICLE_OWNER	    VARCHAR2(50),		
    VEHICLE_NUMBER	    VARCHAR2(10)	PRIMARY KEY,
    VEHICLE_TYPE	    VARCHAR2(50),
    COLOUR	            VARCHAR2(50),		
    IMAGE_OF_VEHICLE	VARCHAR2(500));   
    
CREATE TABLE VEHICLE_TYPE(
    VEHICLE_TYPE	    VARCHAR2(50)	PRIMARY KEY,	
    VEHICLE_CATEGORY	VARCHAR2(50),
    FUEL_TYPE	        VARCHAR2(50)); 
    
CREATE TABLE VEHICLE_LOCATION(
    VEHICLE_NUMBER	NUMBER(10)	PRIMARY KEY,
    LOCATION_NAME	VARCHAR2(50),	
    CITY_NAME	    VARCHAR2(50),	
    PINCODE	        NUMBER(6),	
    COMPLETE_ADDRESS     VARCHAR2(100));
    
CREATE TABLE VEHICLE_PRICE(
    VEHICLE_CATEGORY	VARCHAR2(50)	PRIMARY KEY,
    PRICE_PER_HOUR	NUMBER(8,2),	
    FUEL_TYPE	    VARCHAR(50));

CREATE TABLE BOOKING(
    BOOKING_ID	VARCHAR2(50)	PRIMARY KEY,	
    VEHICLE_NUMBER	NUMBER(10));

CREATE TABLE BOOKING_TIMINGS(
    BOOKING_ID	VARCHAR2(50)	PRIMARY KEY,
    PICK_UP	DATE	NOT NULL,
    DROP_OFF	DATE	NOT NULL,
    BOOKING_DATE	DATE	NOT NULL
);

CREATE TABLE BOOKING_DETAILS(			
BOOKING_ID	VARCHAR2(50)	PRIMARY KEY,
LOCATION_NAME	VARCHAR2(50),		
CUSTOMER_EMAIL	VARCHAR2(50),
AMOUNT_PAID	NUMBER(8,2)	NOT NULL);

CREATE TABLE REQUEST(
    ACTIVITY_TYPE	VARCHAR2(50)	NOT NULL	UNIQUE,
    USER_COMMENTS	VARCHAR2(500),		
    ADMIN_COMMENTS	VARCHAR2(500),		
    VEHICLE_NUMBER	NUMBER(10),
    CUSTOMER_EMAIL	VARCHAR2(50),
    REQUEST_ID	VARCHAR2(50)	PRIMARY KEY);

CREATE TABLE STATUS(
REQUEST_ID	VARCHAR2(50)	PRIMARY KEY	,
REQUEST_STATUS	VARCHAR2(500)	NOT NULL	UNIQUE
);

